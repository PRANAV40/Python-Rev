x, y = 10,3


#Arithmetic operators
print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x // y)
print(x % y)
print(x ** y)



#Comparison operators
print(x > y)
print(x == y)
print(x != y)
print(x >= y)

#Logical operators
a,b  = True,False
print(a and b)
print(a or b)
print(not a)

#Bitwise Operators
print("Bitwise Operators")
print(a & b)
print(a | b)
print(a ^ b)
print(~a)
print(a << 1)
print(a >> 1)
